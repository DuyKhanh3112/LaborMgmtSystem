    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String usernameEmp = jUsername_Emp.getText();
        String date = jDate.getText();
        String shift = jShift.getSelectedItem().toString();
        int i;
        if (usernameEmp.equals("") || date.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter Username's Employee/ Date/ Year");
        } else {
            i = app.createAttendence(Integer.parseInt(usernameEmp), date, shift);
            if (i != 0) {
                JOptionPane.showMessageDialog(rootPane, i + " records inserted");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Add failed. Please re-enter");
            }
        }
        showTable();
    }//GEN-LAST:event_jButton1MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked

        String usernameEmp = jUsername_Emp.getText();
        String date = jDate.getText();
        String shift = jShift.getSelectedItem().toString();

        if (usernameEmp.equals("") || date.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter Username's Employee/ Date/ Year");
        } else {
            if (app.updateShift(Integer.parseInt(usernameEmp), date, shift, getData()) != 0) {
                JOptionPane.showMessageDialog(rootPane, "1 rows affected");
            } else {
                JOptionPane.showMessageDialog(rootPane, "0 rows affected");
            }
        }
        jUsername_Emp.setText("");
        jDate.setText("");
        showTable();
    }//GEN-LAST:event_jButton2MouseClicked


    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        String usernameEmp = jUsername_Emp.getText();
        String date = jDate.getText();
        String shift = jShift.getSelectedItem().toString();

        if (usernameEmp.equals("") || date.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter Username's Employee/ Date/ Year");
        } else {
            if(JOptionPane.showConfirmDialog(rootPane, "Delete")==0){
                app.deleteShift(Integer.parseInt(usernameEmp), date, shift);
            }
        }
        showTable();
    }//GEN-LAST:event_jButton3MouseClicked

